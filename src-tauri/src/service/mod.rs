//! 服务层模块
//!
//! 本模块包含所有业务逻辑实现，是应用程序的核心业务层。
//! 服务层负责处理复杂的业务逻辑，包括数据的 CRUD 操作、事务管理、
//! 数据转换以及跨实体的业务规则实现。
//!
//! # 模块结构
//! - `note`: 笔记服务，处理笔记的增删改查和搜索
//! - `note_history`: 笔记历史服务，记录和查询笔记变更历史
//! - `notebook`: 笔记本服务，管理笔记本的 CRUD 操作
//! - `tag`: 标签服务，管理标签的 CRUD 操作
//!
//! # 设计原则
//! - 所有数据库操作通过服务层进行，保持 command 层的简洁
//! - 使用事务确保数据一致性
//! - 返回 `anyhow::Result` 提供统一的错误处理

/// 应用日志服务模块
///
/// 提供操作审计日志的记录、搜索、脱敏、文件日志管理功能
pub mod app_log;

/// 笔记附件服务模块
///
/// 提供附件的保存、查询、删除功能
pub mod attachment;

/// 笔记服务模块
///
/// 提供笔记的完整 CRUD 功能，包括：
/// - 创建笔记（含标签关联和历史记录）
/// - 更新笔记（自动记录变更历史）
/// - 删除笔记（级联删除标签关联）
/// - 分页搜索（支持笔记本、标签、关键词过滤）
pub mod note;

/// 笔记历史服务模块
///
/// 提供笔记变更历史的查询功能：
/// - 分页查询指定笔记的历史记录
/// - 支持查看创建、更新、删除操作的历史
pub mod note_history;

/// 笔记本服务模块
///
/// 提供笔记本的 CRUD 功能：
/// - 获取所有笔记本（按排序和更新时间排序）
/// - 创建、更新、删除笔记本
pub mod notebook;

/// 标签服务模块
///
/// 提供标签的 CRUD 功能：
/// - 获取所有标签（按排序和更新时间排序）
/// - 创建、更新、删除标签
pub mod tag;

/// 数据库备份与恢复服务模块
///
/// 支持 SQL、Excel、CSV 三种格式的导出和导入
pub mod backup;

/// 应用设置服务模块
///
/// 提供键值对形式的设置存取功能
pub mod settings;

/// 图片存储服务模块
///
/// 将图片保存为本地文件，替代 Base64 内联存储
pub mod image;

/// 加密/解密服务模块
///
/// 提供笔记内容的 AES-256-GCM 加密和解密
pub mod crypto;

/// 笔记链接服务模块
///
/// 实现笔记间的双向链接功能
pub mod note_link;

/// 笔记模板服务模块
///
/// 提供笔记模板的 CRUD 功能
pub mod note_template;

/// 锁屏认证服务模块
///
/// 提供锁屏密码的设置、验证和清除功能
pub mod auth;

/// MCP 访问控制服务模块
///
/// 实现笔记的三层访问控制权限解析
pub mod mcp_access;

/// 系统 Keychain 服务模块
///
/// 使用 OS 原生安全存储管理敏感数据（加密密钥、数据库密码等）
pub mod keychain;

/// Profile 配置管理模块
///
/// 管理多数据库配置的 CRUD 和切换
pub mod profile;

/// 跨 Profile 同步服务模块
///
/// 通过 service 层实现跨数据库同步，保证加密和历史记录一致性
pub mod sync;

/// 同步日志服务模块
///
/// 管理同步记录和明细的 CRUD 操作
pub mod sync_log;
