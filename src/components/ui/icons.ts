// 分类映射
export const iconCategoryMap: Record<string, string[]> = {
  file: Object.keys(iconComponents).filter((name) => name.startsWith('File')),
  folder: Object.keys(iconComponents).filter((name) => name.startsWith('Folder')),
  book: Object.keys(iconComponents).filter(
    (name) => name.startsWith('Book') || name.startsWith('Notebook') || name === 'Library',
  ),
  tag: Object.keys(iconComponents).filter(
    (name) => name.startsWith('Tag') || name.startsWith('Bookmark') || name.startsWith('Flag'),
  ),
  edit: Object.keys(iconComponents).filter(
    (name) =>
      name.startsWith('Pencil') ||
      name.startsWith('Pen') ||
      name.startsWith('Edit') ||
      name === 'Eraser' ||
      name === 'Highlighter',
  ),
  list: Object.keys(iconComponents).filter(
    (name) =>
      name.startsWith('List') ||
      name.startsWith('Check') ||
      name === 'Square' ||
      name === 'SquareCheck',
  ),
  time: Object.keys(iconComponents).filter(
    (name) =>
      name.startsWith('Clock') ||
      name.startsWith('Timer') ||
      name.startsWith('Calendar') ||
      name === 'Hourglass',
  ),
  media: Object.keys(iconComponents).filter(
    (name) =>
      name.startsWith('Image') ||
      name.startsWith('Camera') ||
      name.startsWith('Video') ||
      name.startsWith('Music') ||
      name.startsWith('Mic') ||
      name.startsWith('Volume') ||
      name === 'Film' ||
      name === 'Headphones' ||
      name === 'Speaker',
  ),
  shape: Object.keys(iconComponents).filter(
    (name) =>
      name === 'Star' ||
      name.startsWith('Heart') ||
      name.startsWith('Circle') ||
      name === 'Triangle' ||
      name === 'TriangleAlert' ||
      name === 'Hexagon' ||
      name === 'Pentagon' ||
      name === 'Octagon' ||
      name === 'Diamond' ||
      name === 'Gem' ||
      name === 'Crown' ||
      name.startsWith('Spark') ||
      name.startsWith('Zap') ||
      name === 'Flame' ||
      name === 'Sun' ||
      name === 'Moon' ||
      name.startsWith('Cloud') ||
      name === 'Snowflake' ||
      name === 'Rainbow',
  ),
  user: Object.keys(iconComponents).filter((name) => name.startsWith('User')),
}

// 获取指定分类的图标
export const getIconsByCategory = (category: string): string[] => {
  if (category === 'all') {
    return Object.keys(iconComponents)
  }
  if (category === 'other') {
    const usedIcons = new Set(Object.values(iconCategoryMap).flat())
    return Object.keys(iconComponents).filter((name) => !usedIcons.has(name))
  }
  return iconCategoryMap[category] || []
}
